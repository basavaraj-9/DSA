#include <iostream>
using namespace std;
int main()
{
    int a ,b;
    cout<<"Enter a:";
    cin>>a;     
    cout<<"Enter b:";
    cin>>b;

    cout<<"sum of a ,b "<<(a+b)<<endl;
    cout<<"diff of a, b "<<(a-b)<<endl;
    cout<<"product of a, b "<<(a*b)<<endl;
    cout<<"quotient of a, b "<<(a/b)<<endl;
    cout<<"remainder of a, b "<<(a%b)<<endl;
    return 0;
}